package com.example.a4_17listview;

public class Fruit {
    private String name;
    private int imageID;

    public String getName() {
        return name;
    }

    public int getImageID() {
        return imageID;
    }

    public Fruit(String name, int imageID) {
        this.name = name;
        this.imageID = imageID;
    }
}
